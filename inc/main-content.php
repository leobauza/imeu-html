	<p>Directed by Hany Abu-Assad, perhaps best known for the Academy Award-nominated and winner of the Golden Globe for best foreign language film Paradise Now (2005), Omar has received critical acclaim since premiering at the 2013 Cannes Film Festival, where it received a standing ovation and was awarded a jury prize for Un Certain Regard which recognizes young talent and innovative works.</p>
	<p>This fall Omar will be screened at film festivals across the world, including the Toronto International Film Festival beginning in September and the New York Film Festival beginning in October.</p>
	<p>"I'm overwhelmed on many different levels," says Zuaiter of the film. In addition to this being his first film production with ZBROS LLC, an independent film production company he formed with his brothers, he also played the role of Rami -- an Israeli officer who forms a unique and unexpected relationship with Omar, the title character.</p>

	<blockquote>
		<p>This is a story that just so happens to occur in the Occupied Palestinian Territories -- but it could happen anywhere, it is about basic truths around love, betrayal and trust.</p>
	</blockquote>

	<p>"There is such a large pool of Palestinian talent at every level, the goal was to encourage Palestinians to invest in their own people in this industry, and in jobs," recalls Zuaiter, adding, "It was actually pure coincidence that the film was 95% Palestinian funded, we needed to supplement an investment that fell through and we got the opportunity to bring more Palestinians to join the bigger dream. I am extremely proud."</p>
	<p>A story of two lovers on opposite sides of Israel's separation wall, Omar seeks to humanize rather than demonize the characters. "There's nothing definitive in the film -- good and bad or right and wrong. There are shades of gray. This is what makes the film universal. Complex personalities," explains Zuaiter.</p>
	<p>The film explores universal themes that set the stage for its complex characters and unexpected storyline. "Three themes really compel any story -- every great piece of art explores in some way universal concepts of power, love and violence."</p>
	<p>Trying to produce a film in Palestine provided its share of challenges. "Filming in Palestine, there was less to fall back on," says Zuaiter of the experience. "We were using investors' hard earned money and wanted to make something that people would be proud of. The crews didn't always have all the resources Israeli film crews have -- the film infrastructure isn't the same in Palestine as it is in Israel. This production was an example of Palestinian cinema taken to a new level."</p>

